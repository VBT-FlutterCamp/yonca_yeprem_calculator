class Operation{
  double abstractionOperation(double firstNum, double secondNum){
    return firstNum - secondNum;
  }

  double additionOperation(double firstNum, double secondNum){
    return firstNum + secondNum;
  }

  double multiplicationOperation(double firstNum, double secondNum){
    return firstNum * secondNum;
  }

  double divisionOperation(double firstNum, double secondNum){
    return firstNum / secondNum;
  }

  double modOperation(double firstNum, double secondNum){
    return firstNum % secondNum;
  }

}